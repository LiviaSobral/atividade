    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        if(!fieldTitle.getText().trim().equals("")||!fieldDate.getText().trim().equals("")){
            if(TaskController.registerTask(fieldTitle.getText(), fieldDesc.getText(), fieldDate.getText(), user.getId())){
                JOptionPane.showMessageDialog(this, "Task added");
            }else{
                JOptionPane.showMessageDialog(this, "Failed to make task: verify if Title already exists or if Date is in the correct pattern");
            }            
        }else{
            JOptionPane.showMessageDialog(this, "Insert Data into all fields");
        }
        update();
    }//GEN-LAST:event_buttonAddActionPerformed

    private void buttonRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRemoveActionPerformed
        if(list.getSelectedIndex() != -1){
            TaskController.deleteTask(list.getSelectedIndex());
        }else{
            JOptionPane.showMessageDialog(this, "Select an existing task to preform this action");
        }   
        update();
    }//GEN-LAST:event_buttonRemoveActionPerformed

    private void buttonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUpdateActionPerformed
        if(list.getSelectedIndex() != -1){
            if(optionFinished.isSelected()){
            TaskController.updateStatus(list.getSelectedIndex(), true);
        }else if(optionUnfinished.isSelected()){
            TaskController.updateStatus(list.getSelectedIndex(), false);
        }
        }else{
            JOptionPane.showMessageDialog(this, "Select an existing task to preform this action");
        }        
        update();
    }//GEN-LAST:event_buttonUpdateActionPerformed

