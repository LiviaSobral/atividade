    private void update(){
        model.clear();
        ArrayList<String> tasks = TaskController.findTasks(user.getId());
        for(String task : tasks){
            model.addElement(task);
        }
    }
    private void update(boolean status){
        model.clear();
        ArrayList<String> tasks = TaskController.findTasks(user.getId(),status);
        for(String task : tasks){
            model.addElement(task);
        }
    }
    

